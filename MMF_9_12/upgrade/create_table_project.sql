CREATE TABLE PROJECT (
	ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
	NAME VARCHAR2(255) NOT NULL,
	DESCRIPTION VARCHAR2(255) NOT NULL,
	START_DATE DATE NOT NULL,
	FINISH_DATE DATE NOT NULL,
	CONSTRAINT PROJECT_PK PRIMARY KEY (ID)
);

COMMENT ON COLUMN PROJECT.ID IS 'Unique identifier of the table PROJECT';
COMMENT ON COLUMN PROJECT.NAME IS 'Name of project';
COMMENT ON COLUMN PROJECT.DESCRIPTION IS 'Description of project';
COMMENT ON COLUMN PROJECT.START_DATE IS 'Start date of project';
COMMENT ON COLUMN PROJECT.FINISH_DATE IS 'Finish date of project';
