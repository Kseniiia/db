CREATE TABLE DEVELOPER_TASK (
	ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
	DEVELOPER_ID NUMBER NOT NULL,
	TASK_ID NUMBER NOT NULL,
	CONSTRAINT DEVELOPER_TASK_PK PRIMARY KEY (ID)
);

COMMENT ON COLUMN DEVELOPER_TASK.ID IS 'Unique identifier of the table DEVELOPER_TASK';
COMMENT ON COLUMN DEVELOPER_TASK.DEVELOPER_ID IS 'Developer id';
COMMENT ON COLUMN DEVELOPER_TASK.TASK_ID IS 'Task id';