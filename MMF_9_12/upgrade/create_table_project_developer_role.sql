CREATE TABLE PROJECT_DEVELOPER_ROLE (
	ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
	PROJECT_ID NUMBER NOT NULL,
	DEVELOPER_ID NUMBER NOT NULL,
	ROLE_ID NUMBER NOT NULL,
	CONSTRAINT PROJECT_DEVELOPER_ROLE_PK PRIMARY KEY (ID)
);

COMMENT ON COLUMN PROJECT_DEVELOPER_ROLE.ID IS 'Unique identifier of the table PROJECT_DEVELOPER_ROLE';
COMMENT ON COLUMN PROJECT_DEVELOPER_ROLE.PROJECT_ID IS 'Project id';
COMMENT ON COLUMN PROJECT_DEVELOPER_ROLE.DEVELOPER_ID IS 'Developer id';
COMMENT ON COLUMN PROJECT_DEVELOPER_ROLE.ROLE_ID IS 'Role id';