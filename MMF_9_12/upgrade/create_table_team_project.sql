CREATE TABLE TEAM_PROJECT (
	ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
	TEAM_ID NUMBER,
	PROJECT_ID NUMBER,
	CONSTRAINT TEAM_PROJECT_PK PRIMARY KEY (ID)
);

COMMENT ON COLUMN TEAM_PROJECT.ID IS 'Unique identifier of the table TEAM_PROJECT';
COMMENT ON COLUMN TEAM_PROJECT.TEAM_ID IS 'Team id';
COMMENT ON COLUMN TEAM_PROJECT.PROJECT_ID IS 'Project id';