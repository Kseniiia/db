CREATE TABLE DEVELOPER 
(
	ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
	FIRST_NAME VARCHAR2(255) NOT NULL,
	LAST_NAME VARCHAR2(255) NOT NULL,
	DEPARTMENT_ID NUMBER,
	CONSTRAINT DEVELOPER_PK PRIMARY KEY (ID)
);

COMMENT ON COLUMN DEVELOPER.ID IS 'Unique identifier of the table DEVELOPER';
COMMENT ON COLUMN DEVELOPER.FIRST_NAME IS 'First name';
COMMENT ON COLUMN DEVELOPER.LAST_NAME IS 'Last name';
COMMENT ON COLUMN DEVELOPER.DEPARTMENT_ID IS 'Отдел, в котором работает разработчик';

