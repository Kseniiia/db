CREATE TABLE DEVELOPER_SKILL 
(
	ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
	DEVELOPER_ID NUMBER NOT NULL,
	SKILL_ID NUMBER NOT NULL,
	CONSTRAINT DEVELOPER_SKILL_PK PRIMARY KEY (ID)
);

COMMENT ON COLUMN DEVELOPER_SKILL.ID IS 'Unique identifier of the table DEVELOPER_SKILL';
COMMENT ON COLUMN DEVELOPER_SKILL.DEVELOPER_ID IS 'Developer id';
COMMENT ON COLUMN DEVELOPER_SKILL.SKILL_ID IS 'Skill id';
