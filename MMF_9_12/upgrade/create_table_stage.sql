CREATE TABLE STAGE (
	ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
	NAME VARCHAR2(255) NOT NULL,
	START_DATE DATE NOT NULL,
	FINISH_DATE DATE NOT NULL,
	PROJECT_ID NUMBER NOT NULL,
	CONSTRAINT STAGE_PK PRIMARY KEY (ID)
);

COMMENT ON COLUMN STAGE.ID IS 'Unique identifier of the table STAGE';
COMMENT ON COLUMN STAGE.NAME IS 'Name of stage';
COMMENT ON COLUMN STAGE.START_DATE IS 'Start date of stage';
COMMENT ON COLUMN STAGE.FINISH_DATE IS 'Finish date of stage';
COMMENT ON COLUMN STAGE.PROJECT_ID IS 'Project id';