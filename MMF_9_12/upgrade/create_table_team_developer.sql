CREATE TABLE TEAM_DEVELOPER (
	ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
	TEAM_ID NUMBER,
	DEVELOPER_ID NUMBER,
	CONSTRAINT TEAM_DEVELOPER_PK PRIMARY KEY (ID)
);

COMMENT ON COLUMN TEAM_DEVELOPER.ID IS 'Unique identifier of the table TEAM_DEVELOPER';
COMMENT ON COLUMN TEAM_DEVELOPER.TEAM_ID IS 'Team id';
COMMENT ON COLUMN TEAM_DEVELOPER.DEVELOPER_ID IS 'Developer id';